;The following is the code to initialize and display ASCII characters on a SSD1306 OLED Display.
;The code is written for 8051 but it can be extended for any microcontroller that can implement I2C communication.
;FUNCTIONS USED
;	I2C_start		-
;	I2C_stop		-
;	I2C_send		- @Resources Used - R6,C :@Param - A (Byte to send)
;	DELAY			- @Resources Used - R2,R3
;	OLED_DIS_CHAR	- @Resources Used - R4,C,DPTR,B :@Param - A (ASCII character) :@Functions called - I2C_send, 
;	REP_SEQ			- @Resources Used - R6,C :@Param - DPTR (Pointer to Sequence of bytes), R1 (No. of bytes to send), A (Must be cleared before use) 
;				      :@Functions called - I2C_send, DELAY
; 	CLEAR_SCREEN	- @Resources Used - R5,R4 :@Param - A (Byte to send) :@Functions called - I2C_send
;	OLED COMMAND SEQUENCES - 

ORG 00H
	SDA EQU P2.1
	SCL EQU P2.2
	SETB P1.1 ;Turn Off LED 
	
;CODE BEGINS
	ACALL I2C_start
		;send the sequence to initialize oled display
		MOV DPTR, #OLEDINITSEQ
		MOV R1, #33
		CLR A
		LCALL REP_SEQ
	LCALL I2C_stop
		SETB P1.1
	
	;send the sequence to enable OLED to read DATA to be displayed
	LCALL I2C_start
		MOV DPTR, #OLEDREADSEQ
		MOV R1, #2
		CLR A
		LCALL REP_SEQ
	
	;sending DATA to be displayed	
		
		LCALL CLEAR_SCREEN
		
		MOV TMOD,#20H
		MOV TH1,#0FDH
		MOV SCON, #50H
		CLR TI
		SETB TR1
		MOV R7,#0
		AGAINS:
			CLR RI
			REPEATSS:
				JNB RI,REPEATSS
				MOV A,SBUF
				CJNE A,#'+',CHECKNEXT
				MOV A,#128
				CLR C
				SUBB A,R7
				MOV R7,A
				JZ CONT
				SUBBTRACT:
							MOV A,#00H
							LCALL I2C_send
							DJNZ R7, SUBBTRACT
							MOV R7,#0H
				CONT:
				SJMP AGAINS
				
				CHECKNEXT:
				SETB P1.1
				LCALL DELAY
				LCALL OLED_DIS_CHAR
				MOV A,#6
				ADD A,R7
				MOV R7,A
				SJMP AGAINS		

	LCALL I2C_stop
		SETB P1.1 	;LED OFF
		WAIT: SJMP WAIT	;CODE ENDS

	;Creating the start condition
	I2C_start:
		CLR SDA
		SETB SDA
		SETB SCL
		SETB SCL
		CLR SDA
		CLR SCL
		RET

	;Creating the stop condition
	I2C_stop:
		CLR SCL
		CLR SDA
		SETB SCL
		SETB SDA
		RET
	
	;Creating the send AND receive acknowledge condition
	I2C_send:
		MOV R6, #08;
		REP:
			CLR SCL
			RLC A
			MOV SDA, C
			SETB SCL
			DJNZ R6, REP
		CLR SCL
		SETB SDA
		SETB SCL
		WAIT1:	
			MOV C, SDA
			JC WAIT1
		CLR A
		CLR P1.1
		CLR SCL
		RET

	;Delay of approx 2 seconds
	DELAY:
		MOV R6,#200
		AGAIN: MOV R5,#250
		HERE: NOP
			NOP
			DJNZ R5,HERE
			DJNZ R6,AGAIN
			RET
	
	OLED_DIS_CHAR:
			CLR C
			SUBB A,#32
			MOV R6,A
			MOV DPTR, #OLEDCHARDISP
			JZ CONTINUE
			BT1:
				MOV R5,#6
				BT2:
					INC DPTR
					DJNZ R5,BT2
				DJNZ R6,BT1
			CONTINUE:
			CLR A
			MOV R5,#6
			BT3:MOVC A,@A+DPTR
			ACALL I2C_send
			INC DPTR
			DJNZ R5,BT3
		RET
	
	
	REP_SEQ: 
		MOVC A, @A+DPTR
		ACALL I2C_send
		ACALL DELAY
		SETB P1.1
		ACALL DELAY
		INC DPTR
		DJNZ R1, REP_SEQ
		RET
		
	CLEAR_SCREEN:
		CLR C
			MOV DPTR, #OLEDCHARDISP
			CLR A
			MOV R4,#8
			MARK1: 
				MOV R5,#128
				MARK2:	
				CLR A
				MOVC A,@A+DPTR
				ACALL I2C_send
			DJNZ R5,MARK2
			DJNZ R4,MARK1
		RET
		
		
		
	
